import java.util.Scanner;

public class Main {
    public static void main (String[] args){
        Scanner scanner = new Scanner(System.in);
//        --------------------Задача 1:
//        Напишите программу, которая будет принимать на вход число из консоли и на выход
//        будет выводить сообщение четное число или нет. Для определения четности числа
//        используйте операцию получения остатка от деления (операция выглядит так: '% 2')
        System.out.println("---------Задача 1");
        System.out.print("Введите число: ");
        int n = scanner.nextInt();
        if (n % 2 == 0) {
            System.out.println("Четное число");
        } else {
            System.out.println("Нечетное число");
        }

//        --------------------Задача 2:
//        Для введенного числа t (температура на улице) вывести: Если t>–5, то вывести «Warm».
//        Если –5>= t > –20, то вывести «Normal». Если –20>= t, то вывести «Cold»
        System.out.println("---------Задача 2");
        System.out.print("Введите температуру на улице (t): ");
        int t = scanner.nextInt();
        if (t > -5) {
            System.out.println("Warm");
        } else if (t > -20 && t <= -5) {
            System.out.println("Normal");
        } else if (t <= -20) {
            System.out.println("Cold");
        }

//        --------------------Задача 3:
//        Составьте программу, выводящую на экран квадраты чисел от 10 до 20 включительно
        System.out.println("---------Задача 3");
        for (int i = 10; i <= 20; i++) {
            System.out.println("Квадрат числа " + i + " = " + (i * i));
        }

//        --------------------Задача 4:
//        Необходимо, чтоб программа выводила на экран вот такую последовательность:
//        7 14 21 28 35 42 49 56 63 70 77 84 91 98. В решении используйте цикл while.
        System.out.println("---------Задача 4");
        int k = 7;
        while (k <= 98) {
            System.out.print(k + " ");
            k += 7;
        }
        System.out.println();

//        --------------------Задача *:
//        Напишите программу, где пользователь вводит любое целое положительное число. А
//        программа суммирует все числа от 1 до введенного пользователем числа. Для ввода
//        числа воспользуйтесь классом Scanner. Сделать проверку, чтобы пользователь не мог
//        ввести некорректные данные
        System.out.println("Задача *");
        int number;
        while (true) {
            System.out.print("Введите положительное целое число: ");
            if (scanner.hasNextInt()) {
                number = scanner.nextInt();
                if (number > 0) {
                    break; // корректное число введено, выходим из цикла
                } else {
                    System.out.println("Ошибка: число должно быть положительным. Попробуйте снова.");
                }
            } else {
                System.out.println("Ошибка: введено не число. Попробуйте снова.");
                scanner.next(); // очищаем неверный ввод
            }
        }

        // Вычисляем сумму от 1 до number
        int sum = 0;
        for (int i = 1; i <= number; i++) {
            sum += i;
        }
        System.out.println("Сумма чисел от 1 до " + number + " равна " + sum);

        scanner.close();
    }
}
